//This is typecasting in java simply (datatype)variable; that's all
import java.io.*;
import java.util.*;

class casting {
    public static void main(String args[]) {
        Scanner sc = new Scanner(System.in);
        double d = sc.nextDouble();
        // Typecast and print
        int result=(int)d;
        System.out.println(result);
    }
}
